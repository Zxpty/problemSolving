void ONO(){
	int n, k; read(n, k);
	int calc = n - (k - 1);
	if(calc > 0 and calc % 2 == 1){
		ps("YES");
		for(int i = 0; i < k - 1; i++) cout << "1 ";
		ps(calc);
		return;
	}

	int calc2 = n - 2 * (k - 1);
	if(calc2 > 0 and calc2 % 2 == 0){
		ps("YES");
		for(int i = 0; i < k - 1; i++) cout << "2 ";
		ps(calc2);
		return;
	}

	ps("NO");
}

