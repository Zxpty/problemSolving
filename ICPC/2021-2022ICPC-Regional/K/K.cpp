void GA(){
	int n; cin >> n;
	vector<int> chars(1000);
	vector<string> words;

	for (int i = 0; i < n; i++) {
		string s; cin >> s;
		words.push_back(s);
		chars[s[0] - '0']++;
	}

	for (int i = 0; i < n; i++) {
		vector<int> temp = chars;
		bool palabraPosi = true;

		for (int j = 1; j < words[i].size(); ) {
			if (words[i][0] == words[i][j]) j++;
			if (j == words[i].size()) {
				cout << "Y"; return;
			}
		}

		for (char ch : words[i]) {
			if (temp[ch - '0'] > 0) {
				temp[ch - '0']--;
			} else {
				palabraPosi = false;
				break;
			}
		}
		
		if (palabraPosi) {
			cout << "Y"; return;
		}
	}
	cout << "N";
}

