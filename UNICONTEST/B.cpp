typedef int64_t i64;
const long long MX = 1e9+7;
const int mx = 1e7 + 5;
void GA(){
	long long n, k, s, c1, c2, m; read(n, k, s, c1, c2, m);
	priority_queue<long long> pq;
	long long curr = s;
	for(int i = 0; i < n; i++){
		if(int(pq.size()) < k){
			pq.push(curr);
		}else if(curr < pq.top()){
			pq.pop();
			pq.push(curr);
		}
		curr = (c1 * curr + c2) % m;
	}

	vector<long long> ans;
	while(!pq.empty()){
		ans.push_back(pq.top());
		pq.pop();
	}
	sort(ans.begin(), ans.end());
	for(int i = 0; i < int(ans.size()); i++){
		cout << ans[i] << ' ';
	}
	ps();
}

