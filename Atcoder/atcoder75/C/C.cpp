const int MX = 1e9;

void ONO(){
	int n, m; read(n, m);
	vector<vector<int>> adj(n);
	for(int i = 0; i < m; i++){
		int u, v; read(u, v);
		u--, v--;
		adj[u].push_back(v);
		adj[v].push_back(u);
	}

	vector<int> time(n);
	vector<int> low(n);
	vector<int> vis(n);

	vector<vector<int>> bridges;
	int timer = 0;
	function<void(int, int)> dfs = [&](int node, int parent){
		vis[node] = 1;
		time[node] = low[node] = timer++;
		for(auto v : adj[node]){
			if(v == parent) continue;
			if(!vis[v]){
				dfs(v, node);
				low[node] = min(low[node], low[v]);
				if(low[v] > time[node]){
					bridges.push_back({v, node});
				}
			}else{
				low[node] = min(low[node], low[v]);
			}
		}
	};
	dfs(0, -1);
	int ans = int(bridges.size());
	ps(ans);
}

int main(){
	cpu();
	int t = 1;
	//cin >> t;
	while (t--)
	{
		ONO();
	}
	return 0;
}
